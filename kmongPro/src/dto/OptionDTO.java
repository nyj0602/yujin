package dto;

public class OptionDTO {
	public OptionDTO () {}
	//  OP_SEP / OP_PRICE / OP_TERM / OP_CHANGE/ OP_MANUAL1/OP_MANUAL2
	String op_sep;
	String op_price;
	String op_term;
	String op_change;
	String op_manual1;
	String op_manual2;
	public String getOp_sep() {
		return op_sep;
	}
	public void setOp_sep(String op_sep) {
		this.op_sep = op_sep;
	}
	public String getOp_price() {
		return op_price;
	}
	public void setOp_price(String op_price) {
		this.op_price = op_price;
	}
	public String getOp_term() {
		return op_term;
	}
	public void setOp_term(String op_term) {
		this.op_term = op_term;
	}
	public String getOp_change() {
		return op_change;
	}
	public void setOp_change(String op_change) {
		this.op_change = op_change;
	}
	public String getOp_manual1() {
		return op_manual1;
	}
	public void setOp_manual1(String op_manual1) {
		this.op_manual1 = op_manual1;
	}
	public String getOp_manual2() {
		return op_manual2;
	}
	public void setOp_manual2(String op_manual2) {
		this.op_manual2 = op_manual2;
	}
	
	
}
